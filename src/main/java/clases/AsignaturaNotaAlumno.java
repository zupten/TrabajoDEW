package clases;

public class AsignaturaNotaAlumno {
	private String asignatura, nota;
	
	public AsignaturaNotaAlumno(String asignatura, String nota) {
		this.asignatura = asignatura;
		this.nota = nota;
	}
	
	public String getAsignatura() {
		return asignatura;
	}
	public String getNota() {
		return nota;
	}

}
